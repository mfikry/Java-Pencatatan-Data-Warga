    private boolean check() {
        ArrayList<String> numdata = new ArrayList<String>();

        for(int count = 0; count < tabelModel.getRowCount(); count++){
            numdata.add(tabelModel.getValueAt(count, 1).toString());
        }
        Penduduk pen = (Penduduk)cbxPenduduk.getSelectedItem();
        String x = pen + "";
        for (int i = 0; i < numdata.size(); i++) {
            if (numdata.get(i).equals(x)) {
                return true;
            }
        }
        
        return false;
    }
    
    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Penduduk pen = (Penduduk)cbxPenduduk.getSelectedItem();
        String strTagihan = txttagihan.getText();
        if(txttagihan.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Jumlah tagihan tidak boleh kosong");
            txttagihan.requestFocus();
        } else if (check()) {
            JOptionPane.showMessageDialog(null, "Data sudah ada, dengan nik: " + pen);  
        } else if (StringUtils.isNumeric(strTagihan)) {
            int t = Integer.parseInt(strTagihan);
            int x  = 0;
            IuranPenduduk i = new IuranPenduduk(x, t, pen);
            iController.setDml(i, OperasiCRUD.INSERT);
        } else {
            JOptionPane.showMessageDialog(null, "Input jumlah tagihan harus bilangan integer.");
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

