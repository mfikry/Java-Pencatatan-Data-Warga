    private void tableToForm(){
        int rowIndex = tblIuran.getSelectedRow();
        cbxPenduduk.setSelectedIndex(rowIndex);
        txttagihan.setText(tabelModel.getValueAt(tblIuran.getSelectedRow(),2)+"");
        btnSimpan.setEnabled(false);
        btnTambah.setEnabled(true);
        btnUbah.setEnabled(true);
        btnHapus.setEnabled(true);
    }
    
    private void setTabelIuran(List<IuranPenduduk>daftarIuran){
        if(daftarIuran == null){
            JOptionPane.showMessageDialog(this, "Data Iuran tidak Ada"
                        + "Diload","Gagal Koneksi", JOptionPane.ERROR_MESSAGE);
        }else{
            tabelModel = new DefaultTableModel();
            tabelModel.setColumnIdentifiers(new String[]{
            "Kode Tagihan", "NIK", "Jumlah Tagihan"
        });
            for(IuranPenduduk i : daftarIuran){
                Object[] o = new Object[4];
                o[0] = i.getkode_t();
                o[1] = i.getPenduduk();
                o[2] = i.gettagihan();
                tabelModel.addRow(o);
            }
            tblIuran.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS);
            tblIuran.setModel(tabelModel);
        }
    }
    
    public void update(Observable o, Object arg){
        if (o == iController){
            IuranPenduduk i = (IuranPenduduk)arg;
            if(iController.getCrudState()== OperasiCRUD.INSERT){
                JOptionPane.showMessageDialog(this, "Data Berhasil Di simpan");
            } else if(iController.getCrudState()== OperasiCRUD.UPDATE){
                JOptionPane.showMessageDialog(this,"Data Berhasil di Update");
            } else if(iController.getCrudState()== OperasiCRUD.DELETE){
                JOptionPane.showMessageDialog(this,"Data Berhasil di Hapus");
            }
            setTabelIuran(iController.getAllIuran());
            resetForm();
        }
    }

