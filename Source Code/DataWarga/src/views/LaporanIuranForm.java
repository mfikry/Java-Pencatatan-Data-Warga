    private void data() {
        int row = iuranTable.getRowCount();
        for(int a= 0; a<row;a++){
            tabelModel.removeRow(0);
        }
        try{
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/db_penduduk","root","");
        ResultSet rs = cn.createStatement().executeQuery("SELECT * FROM tiuran inner join tpenduduk on tpenduduk.nik = tiuran.nik");
        while(rs.next()){
            String data []= {rs.getString(3),rs.getString(6),rs.getString(2)};
            tabelModel.addRow(data);
        }
        }
        catch (SQLException ex) {
            Logger.getLogger(LaporanIuranForm.class.getName()).log(Level.SEVERE,null, ex);
        }
    }

