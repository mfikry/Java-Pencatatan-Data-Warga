    private void setTabelPenduduk(List<Penduduk> lsPenduduk){
        if(lsPenduduk == null){
            JOptionPane.showMessageDialog(this, "Penduduk tidak bisa diload",
                    "Gagal Koneksi", JOptionPane.ERROR_MESSAGE);
        }else{
            tabelModel = new DefaultTableModel();
            tabelModel.setColumnIdentifiers (new String[]{
                "Id", "NIK","Nama","Jenis Kelamin","Tempat Lahir","Tanggal Lahir","Agama","Pendidikan","Jenis Pekerjaan","Alamat","Jenis Penduduuk"
            });
            for(Penduduk p : lsPenduduk){
                Object[] o = new Object[11];
                o[0] = p.getid();
                o[1] = p.getnik();
                o[2] = p.getnamapen();
                o[3] = p.getjenis_kelamin();
                o[4] = p.gettempat_lahir();
                o[5] = p.gettanggal_lahir();
                o[6] = p.getagama();
                o[7] = p.getpendidikan();
                o[8] = p.getjenis_pekerjaan();
                o[9] = p.getalamat();
                o[10] = p.getjenis_penduduk();
                tabelModel.addRow(o);
            }
            tblPenduduk.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS);
            tblPenduduk.setModel(tabelModel);
        }
    }
    
