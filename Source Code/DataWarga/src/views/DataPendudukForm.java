    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat Date_Format = new SimpleDateFormat("yyyy-MM-dd");
        
        if(txtnik.getText().equals("")){
            JOptionPane.showMessageDialog(null, "NIK tidak boleh kosong");
            txtnik.requestFocus();
        }
        else if(txtnamapen.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Nama tidak boleh kosong");
            txtnik.requestFocus();
        }else{
        String nik = txtnik.getText();
        String namapen = txtnamapen.getText();
        String jenis_kelamin = txtjenis_kelamin.getText();
        String tempat_lahir = txttempat_lahir.getText();
        String tanggal_lahir = Date_Format.format(dateChooser.getDate());
        String agama = txtagama.getText();
        String pendidikan = txtpendidikan.getText();
        String jenis_pekerjaan = txtjenis_pekerjaan.getText();
        String alamat = txtalamat.getText();
        String jenis_penduduk = (String) cbxJenisPenduduk.getSelectedItem();
        Penduduk p = new Penduduk(nik, namapen,jenis_kelamin,tempat_lahir,tanggal_lahir,agama,pendidikan,jenis_pekerjaan,alamat,jenis_penduduk);
        pController.setDml(p, OperasiCRUD.INSERT);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

