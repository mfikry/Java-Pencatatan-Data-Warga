    public void update(Observable o, Object arg){
        if(o == pController){
            Penduduk p = (Penduduk)arg;
            if(pController.getCrudState()== OperasiCRUD.INSERT){
                JOptionPane.showMessageDialog(this,"Data Berhasil di Simpan");
            }else if(pController.getCrudState()== OperasiCRUD.UPDATE){
                JOptionPane.showMessageDialog(this,"Data Berhasil di Update");
                btnSimpan.setEnabled(true);
                txtnik.setEnabled(true);
            }else if(pController.getCrudState()== OperasiCRUD.DELETE){
                JOptionPane.showMessageDialog(this,"Data Berhasil di Hapus");
                btnSimpan.setEnabled(true);
                txtnik.setEnabled(true);
        }
            setTabelPenduduk(pController.getAllPenduduk());
            resetForm();
        }
    }
