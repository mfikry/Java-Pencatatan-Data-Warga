    private void tableToForm(){
        txtnik.setText(tabelModel.getValueAt(tblPenduduk.getSelectedRow(),1)+"");
        txtnamapen.setText(tabelModel.getValueAt(tblPenduduk.getSelectedRow(),2)+"");
        txtjenis_kelamin.setText(tabelModel.getValueAt(tblPenduduk.getSelectedRow(),3)+"");
        txttempat_lahir.setText(tabelModel.getValueAt(tblPenduduk.getSelectedRow(),4)+"");
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)tabelModel.getValueAt(tblPenduduk.getSelectedRow(),5));
            dateChooser.setDate(date);
        } catch (ParseException ex) {
            Logger.getLogger(DataPendudukForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        txtagama.setText(tabelModel.getValueAt(tblPenduduk.getSelectedRow(),6)+"");
        txtpendidikan.setText(tabelModel.getValueAt(tblPenduduk.getSelectedRow(),7)+"");
        txtjenis_pekerjaan.setText(tabelModel.getValueAt(tblPenduduk.getSelectedRow(),8)+"");
        txtalamat.setText(tabelModel.getValueAt(tblPenduduk.getSelectedRow(),9)+"");
        cbxJenisPenduduk.setSelectedItem(tabelModel.getValueAt(tblPenduduk.getSelectedRow(),10)+"");
        txtnik.setEnabled(false);
        btnSimpan.setEnabled(false);
        btnTambah.setEnabled(true);
        btnUbah.setEnabled(true);
        btnHapus.setEnabled(true);
    }    

